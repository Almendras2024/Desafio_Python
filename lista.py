# Declarar una lista con algunos elementos

mi_lista = [10, 20, 30, 40, 50]

 

# Acceder al tercer elemento

print("Tercer elemento:", mi_lista[2])

 

# Agregar un nuevo elemento al final de la lista

mi_lista.append(60)

print("Lista después de agregar un elemento:", mi_lista)

 

# Eliminar el segundo elemento

del mi_lista[1]

print("Lista después de eliminar el segundo elemento:", mi_lista)

 

# Ordenar la lista en orden inverso

mi_lista.sort(reverse=True)

print("Lista ordenada en orden inverso:", mi_lista)
